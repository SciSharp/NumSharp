using System;
using System.Collections.Generic;
using System.IO;
using Microsoft.VisualStudio.TestTools.UnitTesting;
using NumSharp.IO;

namespace NumSharp.UnitTest.APIs
{
    /// <summary>
    /// Tests for np.load functionality including loading pre-generated NumPy files.
    /// </summary>
    [DoNotParallelize]
    [TestClass]
    public class NpLoadTests
    {
        [TestMethod]
        public void Load_FromBytes_RoundTrip()
        {
            var original = np.array(new int[] { 1, 2, 3, 4, 5 });
            byte[] bytes = np.save(original);

            var loaded = np.load(bytes) as NDArray;

            Assert.IsNotNull(loaded);
            Assert.IsTrue(np.array_equal(original, loaded!));
        }

        [TestMethod]
        public void Load_ExistingNpyFile_1D_Int32()
        {
            // This file was generated by NumPy
            var path = @"data/1-dim-int32_4_comma_empty.npy";

            if (!File.Exists(path))
            {
                // Skip if file doesn't exist
                return;
            }

            var arr = np.load_npy(path);

            Assert.AreEqual(1, arr.ndim);
            Assert.AreEqual(4, arr.size);
            Assert.AreEqual(0, arr.GetInt32(0));
            Assert.AreEqual(1, arr.GetInt32(1));
            Assert.AreEqual(2, arr.GetInt32(2));
            Assert.AreEqual(3, arr.GetInt32(3));
        }

        [TestMethod]
        public void Load_Npz_RoundTrip_MultipleArrays()
        {
            var arr1 = np.array(new int[] { 0, 1, 2, 3 });
            var arr2 = np.array(new double[] { 1.1, 2.2, 3.3 });

            using var ms = new MemoryStream();
            np.SaveNpzToStream(ms, new Dictionary<string, NDArray>
            {
                ["first"] = arr1,
                ["second"] = arr2
            }, compress: false, leaveOpen: true);

            ms.Position = 0;

            using var npz = np.load_npz(ms);

            Assert.AreEqual(2, npz.Count);
            Assert.IsTrue(npz.ContainsKey("first"));
            Assert.IsTrue(npz.ContainsKey("second"));

            Assert.IsTrue(np.array_equal(arr1, npz["first"]));
            Assert.IsTrue(np.allclose(arr2, npz["second"]));
        }

        [TestMethod]
        public void Load_Npz_NestedPaths()
        {
            // Test that paths with slashes work (like "A/B")
            var arr = np.array(new int[] { 1, 2, 3 });

            using var ms = new MemoryStream();
            np.SaveNpzToStream(ms, new Dictionary<string, NDArray>
            {
                ["A/data"] = arr,
                ["B/data"] = arr
            }, compress: false, leaveOpen: true);

            ms.Position = 0;

            using var npz = np.load_npz(ms);

            Assert.AreEqual(2, npz.Count);
            Assert.IsTrue(npz.ContainsKey("A/data"));
            Assert.IsTrue(npz.ContainsKey("B/data"));
        }

        [TestMethod]
        public void Load_Stream_MultipleArrays_Sequential()
        {
            var arr1 = np.arange(10);
            var arr2 = np.arange(20);

            // Write two arrays sequentially to the same stream
            using var ms = new MemoryStream();
            np.save(ms, arr1);
            np.save(ms, arr2);

            // Read them back
            ms.Position = 0;
            var loaded1 = np.load(ms) as NDArray;
            var loaded2 = np.load(ms) as NDArray;

            Assert.IsTrue(np.array_equal(arr1, loaded1!));
            Assert.IsTrue(np.array_equal(arr2, loaded2!));
        }

        [TestMethod]
        public void Load_EmptyStream_ThrowsEndOfStreamException()
        {
            using var ms = new MemoryStream();
            Assert.ThrowsException<EndOfStreamException>(() => np.load(ms));
        }
    }
}
